##Faça um algoritmo utilizando o comando while que mostra 
##uma contagem regressiva na tela,iniciando em 10 e terminando
##em 0. Mostrar uma mensagem “FIM!” após contagem.

cont = 0
while cont <= 10:
    print(cont)
    cont = cont +1  
